import java.util.Scanner;

public class TwoQuestions 
{
	public static void main( String[] args )
	{
		Scanner keyboard = new Scanner(System.in);
		String q1, q2;
		
		System.out.println("TWO QUESTIONS");
		System.out.println("Think of something, and I'll try and guess what it is");
		System.out.println("");
		System.out.println("Question 1) Is it an animal?");
		q1 = keyboard.next();
		System.out.println("Question 2) Is it bigger than a peice of paper?");
		q2 = keyboard.next();
		if( q1.equals ("yes") && q2.equals ("yes") )
		{
			System.out.println("Is it a dog?");
		}
		if( q1.equals ("no") && q2.equals ("no") )
		{
			System.out.println("Is it a pencil?");
		}
		if( q1.equals ("no") && q2.equals ("yes") )
		{
			System.out.println("Is it a T.V?");
		}
		if( q1.equals ("yes") && q2.equals ("no") )
		{
			System.out.println("Is it a hamster?");
		}
	}
}